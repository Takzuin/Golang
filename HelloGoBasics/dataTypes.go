package main

import "fmt"

func main() {
	// Concatenates two strings "Text" and "More Text" and prints the result "TextMore Text"
	fmt.Println("Text" + "More Text")

	// Adds two integers 2 and 8 and prints the result 10
	fmt.Println(2 + 8)

	// Divides 8 by 4 and prints the result 2
	fmt.Println(8 / 4)

	// Evaluates the logical AND operation between true and false, which results in false, and prints it
	fmt.Println(true && false)

	// Evaluates the logical OR operation between true and false, which results in true, and prints it
	fmt.Println(true || false)

	// Evaluates the logical NOT operation on true, which results in false, and prints it
	fmt.Println(!true)
}
